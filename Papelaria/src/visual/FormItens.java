    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if(validaCampos()) {
            int linhaSelecionada = tblListagem.getSelectedRow();
            ItensVenda obj = lstResultado.get(linhaSelecionada);           
            if(!(obj.getProduto().getEstoque() - obj.getQuantidade() < 0)) {
                obj.setVenda(venda);
                obj.getProduto().setEstoque(obj.getProduto().getEstoque() - obj.getQuantidade());
                daoProduto.salvar(obj.getProduto());
                venda.setValor(venda.getValor() + obj.getSubtotal());
                daoVenda.salvar(venda);
                dao.salvar(obj);                                                           
                this.atualizaTabelas();
                this.trataEdicao(false);
                txtEstoque.setText(obj.getProduto().getEstoque()+"");
            }else {
                JOptionPane.showMessageDialog(null, "Não há estoque suficiente desse produto");
            }     
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

