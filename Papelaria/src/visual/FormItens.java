    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if(tblListagem.getRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "Não há registros para serem deletados");
        }else{
            int option = JOptionPane.showConfirmDialog(null, "Deseja excluir esse registro?");
            if(option == JOptionPane.YES_OPTION){
                int linhaSelecionada = tblListagem.getSelectedRow();
                ItensVenda obj = lstResultado.get(linhaSelecionada);
                obj.getProduto().setEstoque(obj.getProduto().getEstoque() + obj.getQuantidade());
                daoProduto.salvar(obj.getProduto());
                venda.setValor(venda.getValor() - obj.getSubtotal());
                daoVenda.salvar(venda);
                dao.deletar(obj);      
                this.trataEdicao(false);
                this.atualizaTabelas(); 
                if(tblListagem.getRowCount() == 0){
                    this.limpaCampos();
                }
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

